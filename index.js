// index.js
// Addition and Subtraction of two numbers

function add(a, b) {
    return a + b;
}

function subtract(a, b) {
    return a - b;
}

// Example usage
const num1 = 5;
const num2 = 7;
console.log(`The sum of ${num1} and ${num2} is:`, add(num1, num2));
console.log(`The difference of ${num1} and ${num2} is:`, subtract(num1, num2));
